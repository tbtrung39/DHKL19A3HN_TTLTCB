n = int(input("Nhập thứ: "))
if n == 1:
    print(n,"Sunday")
elif n == 2:
    print(n,"Monday")
elif n == 3:
    print(n,"Tuesday")
elif n == 4:
    print(n,"Wednesday")
elif n == 5:
    print(n,"Thursday")
elif n == 6:
    print(n,"Friday")
elif n == 7:
    print(n,"Saturday")
else:
    print("Không hợp lệ hãy chạy lại chương trình")